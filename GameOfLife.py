def genesis(gField):
    gKernel = np.array([[1,1,1],[1,0,1],[1,1,1]])
    gNeighbors = convolve(gField, gKernel)
    gN_Living = gNeighbors * gField
    gN_Dead   = gNeighbors * (1 - gField) 
    
    gField_genesis = gField + (gN_Dead == 3)
    gField_genesis = gField_genesis - (gN_Living < 2)*gField - (gN_Living > 3)
    
    return gField_genesis

