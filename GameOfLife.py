def animate(i):
    global gField
    
    if i == 0:
        axes.matshow(gField)
        return
    
    axes.clear()
    axes.matshow(gField)
    
    fig.canvas.draw()
    fig.canvas.flush_events()
    #plt.show()
    
    gField = genesis(gField)
    print(f"Iteration: {i}")

def onclick(event):
    global gField
    global axes
    
    if not event.inaxes == axes:
        return
    
    if event.xdata is None or event.ydata is None:
        print(f"-- Event: Touch Out_of_Field")
        return
    
    x, y = int(round(event.xdata)), int(round(event.ydata))
    
    gField[y,x] = not gField[y,x]
    
    axes.clear()
    axes.matshow(gField)
    fig.canvas.draw()
    fig.canvas.flush_events()
    
    print(f"-- Event: Touch {(x,y)}")

cid = fig.canvas.mpl_connect('button_press_event', onclick)

def playCallback(event):
    ani = animation.FuncAnimation(fig, animate, interval=gTime, frames=gSteps, repeat=False)
    fig.canvas.draw()
    fig.canvas.flush_events()
    #plt.show()
    
    return ani

